    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        msj_text.setText("");
        int selectedRow = car_table.getSelectedRow();
        if (selectedRow == -1) {
            msj_text.setText("Lutfen bir kayit secin");
        } else {
            Long id = Long.parseLong(model.getValueAt(selectedRow, 0).toString());
            String message = "araba kaydini silmek istediginize emin misiniz?";
            String title = "Dikkat";
            int secim = JOptionPane.showConfirmDialog(this, message, title, JOptionPane.OK_CANCEL_OPTION, JOptionPane.WARNING_MESSAGE);
            if (secim == JOptionPane.OK_OPTION) {
                carRepository.delete(id);
                getCars();
                clearArea();
                msj_text.setText("Basariyla silindi");
                //clearInformationText();
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

