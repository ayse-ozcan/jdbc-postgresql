    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String brand = brand_text.getText();
        String modelYear = model_year_text.getText();
        String carModel = car_model_text.getText();
        Car car = new Car(brand, carModel, modelYear);

        Long dealerShipId = Long.parseLong(dealership_id_text.getText());
        car.setDelaerShipId(dealerShipId);
        int selectedRow = car_table.getSelectedRow();
        Long id = (Long) model.getValueAt(selectedRow, 0);
        car.setId(id);

        UpdateCarForm updateCarForm = new UpdateCarForm();
        updateCarForm.transferValues(car);
        //updateCarForm.setCarFromCarPAge(car);

        setVisible(false);
        updateCarForm.setVisible(true);

    }//GEN-LAST:event_jButton1ActionPerformed

